// Rules for varible naming

// Case Sensitivity: Variable names are case-sensitive, meaning "name" and "Name" are considered different variables. 
// No Spaces: Variable names cannot contain spaces. 
// Start with a Letter or Underscore: Variable names must begin with a letter or an underscore ( _ ). 
// Allowed Characters: Variable names can include letters, numbers, and underscores. 
// No Reserved Words: Keywords used by the programming language (like if, for, class) cannot be used as variable names. 
// Descriptive Names: Choose names that accurately reflect the variable's purpose. 
// Pronounceable Names: Variable names should be easy to read and pronounce, aiding in discussions and debugging. 








